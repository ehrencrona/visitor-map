package com.velik.recommend.util;

public class InvalidContentIdException extends Exception {

	public InvalidContentIdException() {
		super();
	}

	public InvalidContentIdException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public InvalidContentIdException(String arg0) {
		super(arg0);
	}

	public InvalidContentIdException(Throwable arg0) {
		super(arg0);
	}

}
